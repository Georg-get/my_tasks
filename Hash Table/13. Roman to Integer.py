# Например, 2 пишется как II римская цифра, просто складывая две единицы. 12пишется как XII, что просто X + II.
# Число 27 записывается как XXVII, то есть XX + V + II.
# Римские цифры обычно пишутся от большей к меньшей слева направо. Однако цифра «четыре» не является цифрой «четыре» IIII.
# Вместо этого число четыре записывается как IV. Поскольку единица стоит перед пятеркой, мы вычитаем ее, получая четыре.
# Тот же принцип применим и к числу девять, которое записывается как IX. Есть шесть случаев, когда используется вычитание:
# I можно поставить перед V(5) и X(10), чтобы получилось 4 и 9.
# X можно поставить перед L(50) и C(100), чтобы получилось 40 и 90.
# C можно поставить перед D(500) и M(1000), чтобы получить 400 и 900.
# Дана римская цифра, преобразуйте ее в целое число.

# Example 1:
# Input: s = "III"
# Output: 3
# Explanation: III = 3.

# Example 2:
# Input: s = "LVIII"
# Output: 58
