### Краткое условие:
# Надо перевести римские цифры в человеческие числа.

# Алгоритм решение задачи:
# 0) Создаем словарь dict ключем римской цифры и со значением человеческой цифры и переменную result с значением 0.
# 0.1) Создаем строку replaceStringS и меняем в строке такие значение:
# c "IV" на "IIII", c "IX" на "VIIII", с "XL" на "XXXX", c "XC" на "LXXXX", с "CD" на "CCCC", с "CM" на "DCCCC".
# 1) Проходимся циклом по строке replaceStringS,
# 1.1) Если ключ i есть в словаре dict, то увеличь значение переменной result на значение ключа i из словаря dict.
# 2) Верни переменную result.

# Сложность:
# 1) Время O(n)
# 2) Память O(n)

s = "III"

def romanToInt(s):
    dict = {
        "I": 1,
        "V": 5,
        "X": 10,
        "L": 50,
        "C": 100,
        "D": 500,
        "M": 1000
    }
    result = 0
    replaceStringS = s.replace("IV", "IIII").replace("IX", "VIIII") \
        .replace("XL", "XXXX").replace("XC", "LXXXX") \
        .replace("CD", "CCCC").replace("CM", "DCCCC")
    # III
    for i in replaceStringS:
        if i in dict:
            result += dict[i]
    # 3
    return result

romanToInt(s)

assert romanToInt(s="III") == 3
assert romanToInt(s="LVIII") == 58
assert romanToInt(s="MCMXCIV") == 1994